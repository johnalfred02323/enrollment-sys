<script type="text/javascript">
  
//for viewing of student info
$(document).on( 'click', '.viewinfo', function (e) {   
  var sisnum= $(this).attr('id');
  $.ajax({
        url:"process.php",
        method:"POST",
        data:{"studentdetails":1,sisnum:sisnum},
        success:function(data){
          var result = $.parseJSON(data);  
          $('#fname').val(result.fname);
          $('#lname').val(result.lname);
          $('#mname').val(result.mname);
          $('#stu-type').val(result.stutype);
          $('#stu-orientation').val(result.stu_orientation);
          $('#connum').val(result.con_num);
          $('#gender').val(result.gender);
          $('#email').val(result.email);
          $('#bday').val(result.bday);
          $('#bdayplace').val(result.bplace);
          $('#nationality').val(result.nat);
          $('#fatname').val(result.fat_name);
          $('#fatocc').val(result.fat_occ);
          $('#fatconnum').val(result.fat_con_num);
          $('#matname').val(result.mat_name);
          $('#matocc').val(result.mat_occ);
          $('#matconnum').val(result.mat_con_num);
          $('#prischname').val(result.p_school_name);
          $('#prischyear').val(result.p_school_year);
          $('#secschname').val(result.s_school_name);
          $('#secschyear').val(result.s_school_year);
          $('#secschstrand').val(result.s_school_strand);
          $('#terschname').val(result.t_school_name);
          $('#terschyear').val(result.t_school_year);
          $('#terschcourse').val(result.t_school_course);
          $('#address').val(result.address);
          $('#city').val(result.city);
          $('#prov').val(result.prov);

          if(result.stu_orientation == 'Freshman')
          {
            $('#tertiary').hide();
          }
          else
          {
            $('#tertiary').show();
          }

        }
      });
});
</script>